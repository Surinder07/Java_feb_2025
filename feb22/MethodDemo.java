package feb22;

public class MethodDemo {

    //  method overloading
    // same name and diff parameters

    // create a multiplication class,
    // show the method overloading implementation

    public int addition(int a, int b){
        return a + b;

    }

    public int addition(int a, int b, int c) {
        return a + b + c;
    }


    public int addition(int a, int b, int c, int d) {
        return a + b + c + d;
    }

}
